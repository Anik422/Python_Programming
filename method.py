course = "Python Programing"
print("Upper :", course.upper())  # .upper() = string upper
print("lower :", course.lower())  # .lower() = string lower
print("title :", course.title())  # .titel() = string first word upper
print("find :", course.find("tho"))  # .find() = find string index number
print("replace :", course.replace("P", "J"))  # .replace = replace value
print("fide string :", "pro" in course)   # string find in string return true and false
